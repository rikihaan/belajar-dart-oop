/**
 * Object adalah hasil instancesiasi dari sebuah class
 * Untuk membuat object kita bisa menggunakan nama class lalu di ikuti dengan ()
 */

class Person {}

void main() {
  // membuat object dari class atau di sebut dengan instancesiasi
  Person person1 = Person();
  var person2 = Person();
  print(person1);
  print(person2);
}
